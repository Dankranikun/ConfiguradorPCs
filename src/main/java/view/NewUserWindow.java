    private void bCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCreateActionPerformed
        String username = tfUsername.getText().trim();
        String password = new String(pfPasswd.getPassword()).trim();
        String passwordRep = new String(pfPasswdRep.getPassword()).trim();

        UserController controller = new UserController();
        try {
            controller.createUser(username, password, passwordRep);
            JOptionPane.showMessageDialog(this, "Usuario creado exitosamente.", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
            bClearActionPerformed(evt);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al crear el usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bCreateActionPerformed

